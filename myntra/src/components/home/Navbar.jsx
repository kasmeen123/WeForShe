import { Box, Typography, Fade } from "@mui/material";
import { useState, useEffect } from "react";
import { keyframes } from "@emotion/react";

const fadeInBounce = keyframes`
  0% {
    opacity: 0;
    transform: translateY(-20px);
  }
  50% {
    opacity: 1;
    transform: translateY(10px);
  }
  100% {
    transform: translateY(0);
  }
`;

const Navbar = () => {
  const [showText, setShowText] = useState(true);
  const [showSecondaryText, setShowSecondaryText] = useState(false);

  useEffect(() => {
    const timeout = setTimeout(() => {
      setShowText(false);
      setShowSecondaryText(true);
    }, 2000);

    return () => clearTimeout(timeout);
  }, []);

  return (
    <Box
      sx={{
        px: 10,
        width: "100%",
        maxWidth: "1200px",
        height: "15vh",
        overflow: "hidden",
        background: "linear-gradient(135deg, #FF3E6C, #FF9A8B, #FF6A88, #FFC3A0)", // Adjusted color gradient
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
        mx: "auto",
        position: "relative",
        borderRadius: "10px",
        boxShadow: "0 4px 20px rgba(0, 0, 0, 0.1)",
      }}
    >
      <Fade in={showText} timeout={1000}>
        <Typography
          variant="h4"
          sx={{
            background: "linear-gradient(135deg, #FF3E6C, #FF9A8B, #FF6A88, #FFC3A0)", // Gradient colors extracted from the image
            WebkitBackgroundClip: "text",
            WebkitTextFillColor: "transparent",
            marginLeft: "-60px",
            fontWeight: 600,
            fontStyle: "italic",
            fontSize: "3.0rem",
            textAlign: "center",
            width: "auto",
            textShadow: "2px 2px 4px rgba(0, 0, 0, 0.8)",
            animation: `${fadeInBounce} 1s ease-in-out`,
            letterSpacing: "1.5px",
          }}
        >
          Welcome to MYNTRA
        </Typography>
      </Fade>

      <Fade in={showSecondaryText} timeout={1000}>
        <Typography
          variant="h6"
          sx={{
            color: "white",
            textAlign: "center",
            marginTop: "10px",
            marginRight: "-250px",
            textShadow: "1px 1px 4px rgba(0, 0, 0, 0.7)",
            fontStyle: "italic",
            letterSpacing: "0.5px",
            fontWeight: 500,
            opacity: showSecondaryText ? 1 : 0,
            transition: "opacity 1s ease-in-out",
            padding: "0 20px", // Add some horizontal padding
            background: "rgba(255, 255, 255, 0.2)", // Subtle background for better contrast
            borderRadius: "5px", // Rounded corners for the background
            fontSize: "1.5rem",
          }}
        >
          Everything You Do, Do It In Style with MYNTRA
        </Typography>
      </Fade>
    </Box>
  );
};

export default Navbar;
